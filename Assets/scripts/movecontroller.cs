using System;
using System.Diagnostics;
using NUnit.Framework;
using Unity.VisualScripting;
using UnityEngine;
using UnityEngine.InputSystem;

public class movecontroller : MonoBehaviour
{

    [SerializeField]
    float walkspeed = 2f;

    [SerializeField]
    float gravmult = 2;

    [SerializeField]
    float jmpforce = 200;
    Vector2 moveinpt;
    CharacterController cont;

    float velY = 0;

    bool hasDoubleJumped;

    [SerializeField]
    float ClimbingPower = 100;


    RaycastHit HitTheWall;

    RaycastHit Sword;
   

    // float hasJumpedTwice;

    



    void Start()
    {
        cont = GetComponent<CharacterController>();
    }

    void Update()
    {
        // grav
        velY += Physics.gravity.y * Time.deltaTime * gravmult;
        if (cont.isGrounded && velY < 0)
        {
            velY = -1;
        }

        // move

        Vector3 movement = transform.forward * moveinpt.y + transform.right * moveinpt.x;

        movement *= walkspeed; // * x y z

        movement.y = velY;

        cont.Move(movement * Time.deltaTime);


        if (Physics.Raycast(transform.position, movement, out HitTheWall, 1f)) //lowkey wall climb
        {
            if (HitTheWall.collider.CompareTag("wall"))
            {
                // print("yey wall");
                velY = ClimbingPower;

                
                // make a V3 so that you can jump up and to the oppisite direction with a streinght

            }
        }


        if (Physics.Raycast(transform.position, movement, out Sword,1f)) // sends a ray in front but therefore does't work well with small objects
        {
             if (Sword.collider.CompareTag("sword"))
            {
               print("sword ooh");
            }
        }
       

    }

    void OnMove(InputValue val)

    {
        moveinpt = val.Get<Vector2>();
    }

    void OnJump(InputValue val)
    {
        

        if (cont.isGrounded)
        {
            hasDoubleJumped = false;
            velY = jmpforce;
            // hasJumpedTwice=0;


        }
        else if (cont.isGrounded == false) //else would also work i think
        {

            //     // if(hasJumpedTwice== 0)
            //     // {
            //     //     velY=jmpforce;
            //     // }

            //     // if(val.isPressed)
            //     // {
            //     //     hasJumpedTwice= 1;
            //     // }

            if (hasDoubleJumped == false)
            {
                velY = jmpforce;
            }

            if (val.isPressed)
            {
                hasDoubleJumped = true;
            }


        }

        

    }


    //working on raycasts for wall jump




}




