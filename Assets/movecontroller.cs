using UnityEngine;
using UnityEngine.InputSystem;

public class movecontroller : MonoBehaviour
{
//  double jmp
    [SerializeField]
    float walkspeed = 2f;

    [SerializeField]
    float gravmult = 2;

    [SerializeField]
    float jmpforce=200;
    Vector2 moveinpt;
    CharacterController cont;

    float velY= 0;

    void Start()
    {
         cont= GetComponent<CharacterController>();
    }

    void Update()
    {
        // grav
        velY += Physics.gravity.y*Time.deltaTime * gravmult;
        if (cont.isGrounded && velY< 0)
        {
            velY= -1;
        }

        // move

        Vector3 movement = transform.forward* moveinpt.y +transform.right * moveinpt.x;

        movement *= walkspeed; // * x y z

        movement.y= velY;

        cont.Move(movement* Time.deltaTime);

    }

    void OnMove(InputValue val)

    {
        moveinpt= val.Get<Vector2>();
    }

    void OnJump(InputValue val)
    {
        if (cont.isGrounded)
        {
            velY= jmpforce;
        }
        
    }
}

